% =========================================================================
% [amp_noise.m] AMP (MDAC) 종합 노이즈 모델링 스크립트 (통합본)
% =========================================================================
% 설명: AMP 내부 및 주변 회로에서 발생하는 3가지 핵심 아날로그 노이즈 성분
%       (열잡음, 플리커 잡음, 기생 공급 잡음)을 분리하여 모델링합니다.
%
% [사용 방법]
% - 메인 스크립트의 1단 종료 후, MDAC 증폭 연산 직전에 run('amp_noise.m') 호출
% - 출력되는 'amp_err' 변수를 잔여 전압(Residue) 증폭 식에 가산하여 사용
% =========================================================================

%% =========================================================================
%% 1. AMP 노이즈 성분별 모델링 (Amplifier Noise Profile)
%% =========================================================================

% (1) AMP 채널 열잡음 (White Noise)
% 트랜지스터의 채널 저항 성분으로 인해 주파수 전역에 걸쳐 발생하는 가장 지배적인 랜덤 잡음
amp_thermal_noise = AMPno * randn(1);

% (2) AMP 플리커 노이즈 (1/f Flicker Noise)
% 반도체 계면의 결함 등으로 인해 저주파 영역에서 커지는 성분
% (메인 시뮬레이션에서 스펙트럼의 스커트 현상이나 저주파 노이즈 플로어를 모사하기 위함)
amp_flicker_noise = AMP_flicker_no * randn(1);

% (3) 기생 및 바이어스 회로 유입 잡음 (Crosstalk / Supply Noise Fluctuation)
% 전원 공급선(VDD)이나 참조 바이어스 회로가 흔들리면서 AMP 입력/출력단에 커플링되는 동적 잡음
amp_v_supply_parasitic_noise = AMP_parasitic_no * randn(1);


%% =========================================================================
%% 2. 최종 에러 합산 (Input-Referred 기준)
%% =========================================================================
% 모든 동적 노이즈 성분들은 서로 독립적이므로 입력단 환산(Input-referred) 기준으로 직가산
amp_err = amp_thermal_noise + amp_flicker_noise + amp_v_supply_parasitic_noise;