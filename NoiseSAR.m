% =========================================================================
% [sar_noise.m] SAR ADC 종합 노이즈 모델링 스크립트 (통합본)
% =========================================================================
% 설명: SAR ADC 내부에서 발생하는 4가지 핵심 아날로그 노이즈 성분
%       (샘플링, 비교기 동적, 킥백, DAC 정착) 및 정적 오프셋을 모델링합니다.
%
% [사용 방법]
% - 메인 스크립트의 SAR 비트 루프(인덱스 i) 내부에서 run('sar_noise.m') 호출
% =========================================================================

%% =========================================================================
%% SAR ADC 노이즈 성분별 모델링
%% =========================================================================
% 메인 루프에서 SAR 비트 결정을 위한 인덱스 'i'가 존재할 때만 실행
if exist('i', 'var')
    
    % (1) KT/C 샘플링 서멀 노이즈 
    % 스위치가 열리는 순간 CDAC에 딱 한 번만 박히므로, 첫 번째 비트(i==1) 결정 시에만 전압 가산
    if i == 1
        sar_sampling_noise = KT_C_no_1st * randn(1);
    else
        sar_sampling_noise = 0; 
    end

    % (2) 비교기 동적 노이즈 (매 클록 래치가 트랜지스터 서멀 잡음으로 출렁이는 성분)
    sar_comp_noise = CMPno_1st * randn(1);

    % (3) 비교기 킥백 노이즈 (래칭 순간 강한 스윙이 입력단 CDAC를 뒤흔드는 역류 잡음)
    sar_kickback_noise = Kickback_no_1st * randn(1);

    % (4) DAC 정착(Settling) 노이즈
    % 고속 구동 시 스위칭 후 전압이 완전히 안정되지 못해 생기는 노이즈.
    % 하위 비트로 갈수록 스텝 전압 크기가 작아지므로 정착 에러 전압도 감쇠하도록 가중치 반영
    sar_dac_settling_noise = DAC_settle_no * (1 / (2^i)) * randn(1);

    % (5) 비교기 정적 오프셋 (공정 미스매치로 인해 발생하는 고정 에러)
    sar_static_offset = CMPoff;

    % SAR 전체 오차 전압 변수 생성
    sar1_err = sar_sampling_noise + sar_comp_noise + sar_kickback_noise + sar_dac_settling_noise + sar_static_offset;
end